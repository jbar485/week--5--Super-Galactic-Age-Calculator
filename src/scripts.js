export class Person {
  constructor(age, life) {
    this.age = age,
    this.life = life,
    this.earth = 0,
    this.mercury = 0,
    this.venus = 0,
    this.mars = 0,
    this.jupiter = 0
  }

  checkType() {
    const result = "";
    if (this.age < this.life) {
      result = "";
    }

      return result;
 }
}
